function add(a, b) {
  return a + b;
}

function sub(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function square(a, b) {
  return a ** b;
}

function modulus(a, b) {
  return a ** b;
}

module.exports = { add, sub, multiply, divide, square, modulus };
